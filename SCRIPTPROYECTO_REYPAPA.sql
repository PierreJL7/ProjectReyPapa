		DROP PROC USP_INSUMO_CATEGORIA
	GO
		CREATE PROC USP_INSUMO_CATEGORIA
			AS
					SELECT I.*,
						CASE
							WHEN I.PRECxUNID_INSU <= 30 THEN 'C'
							WHEN I.PRECxUNID_INSU >= 31 AND I.PRECxUNID_INSU<=900 THEN 'B' 
							WHEN I.PRECxUNID_INSU >=900 THEN 'A'
						END AS [CATEGORIA]
					FROM INSUMO I
					ORDER BY [CATEGORIA] ASC
			GO
			EXEC USP_INSUMO_CATEGORIA
		GO

