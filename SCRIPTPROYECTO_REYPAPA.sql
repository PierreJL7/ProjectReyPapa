	SELECT AVG(DVP.PREXKG_PROD) AS 'PROMEDIO DEL PRECIO X KG' 
		FROM DETA_VENTA_PROD AS DVP
	GO

/*4.- SE SOLICITA CONOCER EL KILOMETRAJE TOTAL QUE SE RECORRIO EN CADA ENVIO, SE TIENE QUE ESPECIFICAR LA PLACA DEL VEHICULO,
JUNTO CON EL HILOMETRAJE DE LLEGARA Y SALIDA*/

	SELECT AV.PLACA_VEHICULO       AS 'VEHICULO',
		   CAV.KILOMETRAJE_SALIDA  AS 'KILOMETRAJE DE SALIDA',
		   CAV.KILOMETRAJE_LLEGADA AS 'KILOMETRAJE DE LLEGADA',
		   (CAV.KILOMETRAJE_LLEGADA - CAV.KILOMETRAJE_SALIDA) AS 'TOTAL DE KILOMETRAJE RECORRIDO EN ESTE ENVIO'
		   FROM ASIGN_VEHICULAR    AS AV 
				INNER JOIN (SELECT CAST((KILOM_SAL_ASIG_VEHI)  AS INT) AS [KILOMETRAJE_SALIDA],
						           CAST((KILOM_LLEG_ASIG_VEHI) AS INT) AS [KILOMETRAJE_LLEGADA],
						                 COD_CARAC_ASIG_VEHI
				                   FROM CARAC_ASIG_VEHI) AS CAV ON CAV.COD_CARAC_ASIG_VEHI = AV.COD_CARAC_ASIG_VEHI
	GO
