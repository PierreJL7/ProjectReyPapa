				INNER JOIN TRAB_SERVICIO_CLIENTE AS SC ON SC.COD_TRABAJADOR = T.COD_TRABAJADOR) AS 'CANT. DE TRABAJADORES SERVICIO AL CLIENTE',

		(SELECT COUNT(L.COD_TRAB_LOGISTICA) 
			FROM TRABAJADOR AS T
				INNER JOIN TRAB_LOGISTICA AS L ON L.COD_TRABAJADOR = T.COD_TRABAJADOR) AS 'CANT. DE TRABAJADORES LOGISTICA', 

		(SELECT COUNT(P.COD_TRAB_PRODUCCION) 
			FROM TRABAJADOR AS T
				INNER JOIN TRAB_PRODUCCION AS P ON P.COD_TRABAJADOR = T.COD_TRABAJADOR) AS 'CANT. DE TRABAJADORES PRODUCCION'
	GO

/*2.-SE SOLICITA SABER QUE FORMA DE PAGO FUE LA MAS USADA; SEGÚN EL RANGO DE LA EMPRESA DEBE SER MAYOR DE DIEZ */

	SELECT DESCP_FORMAPAGO, COUNT (*) AS 'CANTIDAD DE FORMAS DE PAGO'
		FROM FORMA_PAGO
		GROUP BY DESCP_FORMAPAGO
		HAVING COUNT(*)>10
	GO

